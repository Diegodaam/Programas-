    private void jButtonRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRegistrarActionPerformed
        for(int i = 0; i<entretenimiento.length; i++){
            nombre = jTextFieldNombre.getText();
            codigo = Integer.parseInt(jTextFieldCodigo.getText());
            planta = jTextFieldPlanta.getText();
            comentarios = jTextAreaComentarios.getText();
            
            String radioSeleccionado = "";
            String checkSeleccioando = "";
            
            if(jRadioButtonMatutino.isSelected()){
                radioSeleccionado = "Entretenimiento";
            }else if(jRadioButtonDiurno.isSelected()){
                radioSeleccionado = "Entrenamiento";
            }else if(jRadioButtonMixto.isSelected()){
                radioSeleccionado = "Departamental";
            }
            
            if(jCheckBoxEntretenimiento.isSelected()){
                checkSeleccioando += "Entretenimiento";
            }
            if(jCheckBoxEntrenamiento.isSelected()){
                checkSeleccioando += "Entrenamiento";
            }
            if(jCheckBoxDepartamental.isSelected()){
                checkSeleccioando += "Departamental";
            }
            
            entretenimiento[i] = new Plaza(nombre, codigo, comentarios, radioSeleccionado, checkSeleccioando, planta);
            
            jLabelMensaje.setText("Registrado con exito");     
            return;
        }
    }//GEN-LAST:event_jButtonRegistrarActionPerformed

