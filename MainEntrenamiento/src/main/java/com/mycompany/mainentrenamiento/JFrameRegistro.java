    private void jButtonMostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonMostrarActionPerformed
        for (int i = 0; i < entretenimiento.length; i++) {
        if (entretenimiento[i] != null) { // Si hay un registro válido
            
            JFrameMostrarInformacion mostrar = new JFrameMostrarInformacion(
                entretenimiento[i].getNombre(),
                entretenimiento[i].getCodigo(),
                entretenimiento[i].getComentarios(),
                entretenimiento[i].getOpcionRadio(),
                entretenimiento[i].getOpcionCheck(),
                entretenimiento[i].getPlanta()
            );
            mostrar.setVisible(true); // Muestra la nueva ventana
            this.dispose(); // Opcional: Cierra la ventana actual si es necesario
            return;
        }
    }

    // Si no hay datos registrados
    jLabelMensaje.setText("No hay datos registrados.");

    }//GEN-LAST:event_jButtonMostrarActionPerformed

