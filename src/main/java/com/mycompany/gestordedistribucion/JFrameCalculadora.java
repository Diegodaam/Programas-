    private void jButtonIgualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonIgualActionPerformed
       
        num2 = Integer.parseInt(jTextArea1.getText());
        
        if(jLabel1.getText().equals("Sumando")){
            int resultado = num1 + num2;
            jTextArea1.setText(String.valueOf(resultado));
        }
        if(jLabel1.getText().equals("Restando")){
            int resultado = num1 - num2;
            jTextArea1.setText(String.valueOf(resultado));
        }
        if(jLabel1.getText().equals("Multiplicando")){
            int resultado = num1 * num2;
            jTextArea1.setText(String.valueOf(resultado));
        }
        if(jLabel1.getText().equals("Dividiendo")){
            int resultado = num1 / num2;
            jTextArea1.setText(String.valueOf(resultado));
        }
        if(jLabel1.getText().equals("Potencia")){
            double resultado = Math.pow(num1, num2);
            jTextArea1.setText(String.valueOf(resultado));
        }
        if(jLabel1.getText().equals("Modulo")){
            int resultado = num1 % num2;
            jTextArea1.setText(String.valueOf(resultado));
        }
        
        jLabel1.setText("");
    }//GEN-LAST:event_jButtonIgualActionPerformed

